#ifndef VEC_H
#define VEC_H

#include <algorithm>
#include <cassert>
#include <vector>

class Vec
{
  public:
    // -------------------------------------------------------------------------
    // Construction
    // -------------------------------------------------------------------------
    Vec() = default;
    explicit Vec(int n) : data_(n, 0.0) {}

    // -------------------------------------------------------------------------
    // Access
    // -------------------------------------------------------------------------
    int size() const { return data_.size(); }
    
    double operator[](int i) const
    {
      assert(i >= 0 && i < size());
      return data_[i];
    }

    // -------------------------------------------------------------------------
    // Modification
    // -------------------------------------------------------------------------
    double& operator[](int i)
    {
      assert(i >= 0 && i < size());
      return data_[i];
    }

    void fill(double value)
    {
      std::fill(data_.begin(), data_.end(), value);
    }

    // -------------------------------------------------------------------------
    // Operations
    // -------------------------------------------------------------------------
    
  private:

    // -------------------------------------------------------------------------
    // Data
    // -------------------------------------------------------------------------
    std::vector<double> data_;
};

#endif