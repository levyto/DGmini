#ifndef ELEMENT1D_H
#define ELEMENT1D_H

// -----------------------------------------------------------------------------
// Description: Single 1D element
// -----------------------------------------------------------------------------
class Element1D
{
  public:
    // -------------------------------------------------------------------------
    // Construction
    // -------------------------------------------------------------------------
    Element1D(double x_left, double x_right) ;

    // -------------------------------------------------------------------------
    // Access
    // -------------------------------------------------------------------------
    double left()     const { return x_left_;            }
    double right()    const { return x_right_;           }

    // -------------------------------------------------------------------------
    // Modification
    // -------------------------------------------------------------------------

    // -------------------------------------------------------------------------
    // Operations
    // -------------------------------------------------------------------------
    double length()   const { return x_right_ - x_left_; }
    double jacobian() const { return 0.5 * length();     }
    double mapToPhysical(double xi) const;
    double mapToReference(double xi) const;

  private:
    // -------------------------------------------------------------------------
    // Data
    // -------------------------------------------------------------------------
    double x_left_;
    double x_right_;
};

#endif