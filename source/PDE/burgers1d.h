// -----------------------------------------------------------------------------
//              DGmini, a minimal 1D discontinuous Galerkin solver
// -----------------------------------------------------------------------------
//
// Description: Class for 1D Burgers' PDE, i.e. u_t + (1/2) * (u^2)_x = 0
//
// -----------------------------------------------------------------------------

#ifndef BURGERS1D_H
#define BURGERS1D_H

#include <cmath>

#include "pde.h"

class Burgers1D : public PDE
{
  public:
    // -------------------------------------------------------------------------
    // Construction
    // -------------------------------------------------------------------------
    Burgers1D() = default;

    // -------------------------------------------------------------------------
    // Access
    // -------------------------------------------------------------------------
    double convectiveFlux(double state) const override
    {
      return 0.5 * state * state;
    }

    // -------------------------------------------------------------------------
    // Modification
    // -------------------------------------------------------------------------

    // -------------------------------------------------------------------------
    // Operations
    // -------------------------------------------------------------------------
    double maxConvectiveEigenvalue(double state) const override
    {
      return std::abs(state);
    }

  private:
    // -------------------------------------------------------------------------
    // Data
    // -------------------------------------------------------------------------
};

#endif
